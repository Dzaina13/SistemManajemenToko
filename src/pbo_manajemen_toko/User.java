    public void tampil_tabel_user() {
        // membuat objek model pada tabel
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Id User");
        tb.addColumn("Username");
        tb.addColumn("Nama");
        tb.addColumn("Role");
        tb.addColumn("Password");
        tb.addColumn("Terakhir Diperbarui");
        
        try {
            // 1. Query
            String query = "SELECT * FROM users";
            
            // 2. Fungsi Koneksi
            java.sql.Connection Vconn = (Connection)KoneksiDB.configDB();
            
            // 3. Kirim parameter fungsi java ke SQL
            java.sql.Statement s = Vconn.createStatement();
            
            // 4. Eksekusi Query
            java.sql.ResultSet r = s.executeQuery(query);
            
            // 5. Looping
            while (r.next()) {
                tb.addRow(new Object[] {
                r.getString(1),r.getString(2),r.getString(3),r.getString(5),r.getString(4),r.getString(6)
                });
            }
            tabel_user.setModel(tb);
            
        } catch (Exception e) {
            
        }
        
    }
    
