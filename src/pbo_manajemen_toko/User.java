    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int baris = tabel_user.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!");
            return;
        }
        
        //ambil id dari tabel
        String id = tabel_user.getValueAt(baris,0).toString();
        String username = tabel_user.getValueAt(baris,1).toString();
        
        int konfirmasi = JOptionPane.showConfirmDialog(
                null,
                "Yakin ingin menghapus data ini?", 
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                java.sql.Connection Vconn = (Connection)KoneksiDB.configDB();
                
                //tambah ke histori
                String aktivitas = "menghapus data user:"+username;
                String logQuery = "INSERT INTO history (id_user, aktivitas) VALUES (?, ?)";
                java.sql.PreparedStatement logS = Vconn.prepareStatement(logQuery);
                logS.setInt(1, Login.Session.getUserId());
                logS.setString(2, aktivitas);
                logS.executeUpdate();
                
                String query = "DELETE FROM users WHERE id_user=?";
                java.sql.PreparedStatement s = Vconn.prepareStatement(query);
                s.setString(1, id);
                s.executeUpdate();
                
                tampil_tabel_user();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Menghapus Data");
                JOptionPane.showMessageDialog(this, e.getMessage()); 
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

